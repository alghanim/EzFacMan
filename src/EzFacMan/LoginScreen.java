/**
 * Checks is there is a valid connection and if the username and password exists
 * then opens the EzFac UI
 * 
 * @throws SQLException
 * @throws ClassNotFoundException
 * @throws Exception 
 */
    private void login() throws SQLException, ClassNotFoundException, Exception {
        EZFacUI ez = new EZFacUI();
        Boolean test = true;

        try {
            ConnectDB.isValidConnection(ConnectDB.getConnection(), "mysql");
        } catch (SQLException e) {
            //ErrorBox.setLocationRelativeTo(null);
            //ErrorBox.setVisible(true);
            test = false;
        }
        if (test) {
            if(ConnectDB.checkifuserexists()) {
                ez.setTitle("EZ-Fac");
                ez.setVisible(true);
                dispose();
            } else   JOptionPane.showMessageDialog(null, "Username/Password incorrect", "Error!", JOptionPane.OK_OPTION);
        } else {
            JOptionPane.showMessageDialog(null, "Database Error", "Error!", JOptionPane.OK_OPTION);
        }
    }

