    /**
     * previous button
     * @param evt when previous is clicked
     */
    private void previousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_previousActionPerformed
       try {
            if(rs.previous()){
                showRoomInfo();
            }else
                
               previous.setEnabled(false);
             } catch (SQLException ex) {
            Logger.getLogger(CommentSearch.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_previousActionPerformed
/**
 * next button
 * @param evt when next is clicked
 */
    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
         try {
            if(rs.next()){
                showRoomInfo();
                 previous.setEnabled(true);
            }else{
                
            
                rs.previous();
            JOptionPane.showMessageDialog(null, "No More results");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CommentSearch.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_nextActionPerformed
/**
 * Search button
 * @param evt when search is clicked
 */
    private void SeatchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeatchBtnActionPerformed

        try {
            rs = RoomsManager.getByComment(commentField.getText());

            if (rs != null) {

                if (rs.next()) {
                    showRoomInfo();

                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(CommentSearch.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CommentSearch.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_SeatchBtnActionPerformed

